BUSINESS INSIGHT 1

Finding:
Patients with high prior healthcare utilization had the
highest readmission rate (21.89%).

Business Impact:
Patients with repeated healthcare interactions are at
significantly higher risk of returning after discharge.

Recommendation:
Implement targeted care management programs and regular
follow-up for high-utilization patients.

---------------------------------------------------------

BUSINESS INSIGHT 2

Finding:
Frequent hospital visitors had a readmission rate of
21.35%, nearly double that of normal visitors (10.71%).

Business Impact:
A small group of patients contributes disproportionately
to hospital readmissions and resource utilization.

Recommendation:
Create a high-risk patient monitoring program focused on
frequent hospital visitors.

---------------------------------------------------------

BUSINESS INSIGHT 3

Finding:
Patients with hospital stays of 8 or more days had the
highest readmission rate (13.38%).

Business Impact:
Longer stays often indicate severe illness or complex
clinical conditions.

Recommendation:
Provide enhanced discharge planning and post-discharge
care coordination for long-stay patients.

---------------------------------------------------------

BUSINESS INSIGHT 4

Finding:
Patients with high clinical complexity had a readmission
rate of 12.06%, compared to only 6.97% for low-complexity
patients.

Business Impact:
Multiple diagnoses increase treatment complexity and
the likelihood of future hospitalizations.

Recommendation:
Develop multidisciplinary care plans for patients with
multiple chronic conditions.

---------------------------------------------------------

BUSINESS INSIGHT 5

Finding:
Patients with high medication burden had the highest
readmission rate (12.78%).

Business Impact:
Managing multiple medications increases the risk of
medication errors and non-adherence.

Recommendation:
Strengthen medication reconciliation and patient
education before discharge.

---------------------------------------------------------

BUSINESS INSIGHT 6

Finding:
Senior citizens experienced a higher readmission rate
(11.85%) compared to non-senior patients (10.59%).

Business Impact:
Older adults require additional support after discharge
to prevent avoidable readmissions.

Recommendation:
Introduce follow-up calls, home-care support, and
specialized discharge planning for elderly patients.

---------------------------------------------------------

BUSINESS INSIGHT 7

Finding:
Female and male patients showed similar readmission
rates (11.25% vs 11.06%).

Business Impact:
Gender is not a major driver of readmission risk.

Recommendation:
Focus intervention strategies on clinical and utilization
factors rather than demographic characteristics.

---------------------------------------------------------

BUSINESS INSIGHT 8

Finding:
Readmission rates were relatively consistent across
racial groups, with only minor differences observed.

Business Impact:
Race alone was not identified as a significant predictor
of readmission in this dataset.

Recommendation:
Prioritize clinical risk factors over demographic factors
when designing intervention programs.

---------------------------------------------------------

PROJECT CONCLUSION

Analysis of 101,763 diabetic patient encounters identified
the strongest drivers of hospital readmissions as:

1. High Prior Healthcare Utilization
2. Frequent Hospital Visits
3. Long Hospital Stays
4. High Clinical Complexity
5. High Medication Burden
6. Advanced Age

By focusing care management efforts on these high-risk
groups, hospitals can potentially reduce 30-day
readmissions, improve patient outcomes, and optimize
healthcare resource utilization.

